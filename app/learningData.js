function fallbackUsage(node) {
  const examples = node.examplePool?.length ? node.examplePool.slice(0, 4) : ["これは ほんです。", "これは ほんですか。"];
  return {
    transformations: [
      {
        title: "예문을 활용형으로 관찰하기",
        base: node.title,
        steps: examples.map((example, index) => ({
          label: ["예문", "변형", "질문", "응용"][index] || "응용",
          text: `${example} 한국어 뜻을 떠올리고, 단어 하나를 바꿔 다시 말합니다.`,
        })),
      },
    ],
    substitutionExamples: [
      {
        title: "단어 바꿔 말하기",
        frame: "예문 안의 핵심 단어를 ___ 로 바꿉니다.",
        slots: (node.vocab || []).slice(0, 6),
        examples: examples.slice(0, 3),
      },
    ],
  };
}

function enrichGrammarLecture(node, lecture) {
  const usage = grammarUsageByNode[node.id] || grammarUsageByLevel[node.level] || fallbackUsage(node);
  return {
    ...lecture,
    transformations: usage.transformations || fallbackUsage(node).transformations,
    substitutionExamples: usage.substitutionExamples || fallbackUsage(node).substitutionExamples,
  };
}


function fillQuiz(items, id) {
  const merged = [...items];
  let index = 0;
  while (merged.length < 18) {
    const base = quizFillers[(index + id.length) % quizFillers.length];
    merged.push({ ...base, q: `${base.q} (${merged.length + 1})` });
    index += 1;
  }
  return merged;
}

